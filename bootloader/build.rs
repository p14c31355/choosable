//! Build script: generates bootloader binaries as Rust byte arrays.
//!
//! Outputs `src/generated.rs` (written to `$OUT_DIR`):
//! - `BOOT_IMG`: 440-byte MBR boot sector (x86-16 machine code)
//! - `STAGE2_BIN`: BIOS stage2 flat binary (real→protected mode, x86-32)
//! - `EFI_BIN`: UEFI PE binary (x86_64-unknown-uefi, cross-compiled by cargo)
//!
//! No external tools (nasm, gcc, linker script) required.
//! All x86 machine code is constructed from pure Rust opcode tables.

use std::env;
use std::fs;
use std::path::PathBuf;

fn main() {
    let out_dir = PathBuf::from(env::var("OUT_DIR").unwrap());
    fs::create_dir_all(out_dir.join("out")).ok();
    let dest = out_dir.join("generated.rs");

    let boot_img = build_mbr_boot_sector();
    let stage2_bin = build_stage2_binary();
    let efi_bin = build_efi_binary(&out_dir);

    let code = format!(
        r#"// AUTO-GENERATED by build.rs — DO NOT EDIT

/// MBR boot sector (440 bytes of x86 real-mode machine code).
pub const BOOT_IMG: &[u8] = &{boot_img:?};

/// Stage 2 BIOS bootloader flat binary (x86-32 protected mode).
pub const STAGE2_BIN: &[u8] = &{stage2_bin:?};

/// UEFI bootloader PE binary (BOOTX64.EFI).
pub const EFI_BIN: &[u8] = &{efi_bin:?};
"#,
    );

    fs::write(&dest, &code).unwrap();
    println!("cargo:rerun-if-changed=build.rs");
    println!("cargo:rerun-if-changed=efi/src/main.rs");
    println!("cargo:rerun-if-changed=efi/Cargo.toml");
}

// ═══════════════════════════════════════════════════════════════════════════
//  x86-16 instruction encoder (real mode)
// ═══════════════════════════════════════════════════════════════════════════

struct CodeBuf {
    bytes: Vec<u8>,
}

impl CodeBuf {
    fn new() -> Self {
        CodeBuf { bytes: Vec::with_capacity(512) }
    }

    fn offset(&self) -> u16 { self.bytes.len() as u16 }

    fn emit(&mut self, b: u8) -> &mut Self { self.bytes.push(b); self }

    fn emit16(&mut self, w: u16) -> &mut Self {
        self.bytes.push(w as u8);
        self.bytes.push((w >> 8) as u8);
        self
    }

    fn emit32(&mut self, d: u32) -> &mut Self {
        self.bytes.push(d as u8);
        self.bytes.push((d >> 8) as u8);
        self.bytes.push((d >> 16) as u8);
        self.bytes.push((d >> 24) as u8);
        self
    }

    // ── Basic ─────────────────────────────────────────────────────
    fn cli(&mut self) -> &mut Self { self.emit(0xFA) }
    fn sti(&mut self) -> &mut Self { self.emit(0xFB) }
    fn cld(&mut self) -> &mut Self { self.emit(0xFC) }
    fn hlt(&mut self) -> &mut Self { self.emit(0xF4) }
    fn nop(&mut self) -> &mut Self { self.emit(0x90) }
    fn ret(&mut self) -> &mut Self { self.emit(0xC3) }

    // ── XOR ───────────────────────────────────────────────────────
    fn xor_ax_ax(&mut self) -> &mut Self { self.emit(0x31).emit(0xC0) }
    fn xor_bx_bx(&mut self) -> &mut Self { self.emit(0x31).emit(0xDB) }

    // ── MOV seg, r16 ──────────────────────────────────────────────
    fn mov_ss_ax(&mut self) -> &mut Self { self.emit(0x8E).emit(0xD0) }
    fn mov_ds_ax(&mut self) -> &mut Self { self.emit(0x8E).emit(0xD8) }
    fn mov_es_ax(&mut self) -> &mut Self { self.emit(0x8E).emit(0xC0) }

    // ── MOV r16, imm16 ────────────────────────────────────────────
    fn mov_sp(&mut self, v: u16) -> &mut Self { self.emit(0xBC).emit16(v) }
    fn mov_si(&mut self, v: u16) -> &mut Self { self.emit(0xBE).emit16(v) }
    fn mov_di(&mut self, v: u16) -> &mut Self { self.emit(0xBF).emit16(v) }
    fn mov_cx(&mut self, v: u16) -> &mut Self { self.emit(0xB9).emit16(v) }
    fn mov_bp(&mut self, v: u16) -> &mut Self { self.emit(0xBD).emit16(v) }
    fn mov_ax(&mut self, v: u16) -> &mut Self { self.emit(0xB8).emit16(v) }
    fn mov_bx(&mut self, v: u16) -> &mut Self { self.emit(0xBB).emit16(v) }

    // ── MOV AH/AL, imm8 ───────────────────────────────────────────
    fn mov_ah(&mut self, v: u8) -> &mut Self { self.emit(0xB4).emit(v) }
    fn mov_al(&mut self, v: u8) -> &mut Self { self.emit(0xB0).emit(v) }

    // ── MOV r8, [r16+disp8] ───────────────────────────────────────
    fn mov_dl_bx_disp8(&mut self, d: i8) -> &mut Self {
        self.emit(0x8A).emit(0x57).emit(d as u8)
    }

    // ── MOV byte [disp16], imm8 ───────────────────────────────────
    fn mov_mem8_imm8(&mut self, addr: u16, v: u8) -> &mut Self {
        self.emit(0xC6).emit(0x06).emit16(addr).emit(v)
    }
    fn mov_mem8_dl(&mut self, addr: u16) -> &mut Self {
        self.emit(0x88).emit(0x16).emit16(addr)
    }
    fn mov_dl_mem16(&mut self, addr: u16) -> &mut Self {
        self.emit(0x8A).emit(0x16).emit16(addr)
    }

    // ── MOV word [disp16], imm16 ──────────────────────────────────
    fn mov_mem16_imm16(&mut self, addr: u16, v: u16) -> &mut Self {
        self.emit(0xC7).emit(0x06).emit16(addr).emit16(v)
    }

    // ── MOV dword [disp16], imm32 ─────────────────────────────────
    fn mov_mem32_imm32(&mut self, addr: u16, v: u32) -> &mut Self {
        self.emit(0x66).emit(0xC7).emit(0x06).emit16(addr).emit32(v)
    }

    // ── CMP byte [disp16], imm8 ───────────────────────────────────
    fn cmp_mem8_imm8(&mut self, addr: u16, v: u8) -> &mut Self {
        self.emit(0x80).emit(0x3E).emit16(addr).emit(v)
    }

    // ── TEST CL, imm8 ─────────────────────────────────────────────
    fn test_cl(&mut self, v: u8) -> &mut Self {
        self.emit(0xF6).emit(0xC1).emit(v)
    }

    // ── SHR word [disp16], 1 ──────────────────────────────────────
    fn shr_mem16_1(&mut self, addr: u16) -> &mut Self {
        self.emit(0xD1).emit(0x2E).emit16(addr)
    }

    // ── REP MOVSW ─────────────────────────────────────────────────
    fn rep_movsw(&mut self) -> &mut Self { self.emit(0xF3).emit(0xA5) }
    fn lodsb(&mut self) -> &mut Self { self.emit(0xAC) }
    fn test_al_al(&mut self) -> &mut Self { self.emit(0x08).emit(0xC0) }

    // ── INT imm8 ──────────────────────────────────────────────────
    fn int(&mut self, n: u8) -> &mut Self { self.emit(0xCD).emit(n) }

    // ── Jumps (relative) ──────────────────────────────────────────
    fn jmp_short(&mut self, rel: i8) -> &mut Self { self.emit(0xEB).emit(rel as u8) }
    fn jmp_near(&mut self, rel: u16) -> &mut Self { self.emit(0xE9).emit16(rel) }
    fn jmp_far(&mut self, seg: u16, off: u16) -> &mut Self {
        self.emit(0xEA).emit16(off).emit16(seg)
    }

    // Conditional jumps (emit placeholder rel8=0, caller patches)
    fn jc_placeholder(&mut self) -> u16  { let p=self.offset(); self.emit(0x72).emit(0x00); p }
    fn jnc_placeholder(&mut self) -> u16 { let p=self.offset(); self.emit(0x73).emit(0x00); p }
    fn jne_placeholder(&mut self) -> u16 { let p=self.offset(); self.emit(0x75).emit(0x00); p }
    fn jz_placeholder(&mut self) -> u16  { let p=self.offset(); self.emit(0x74).emit(0x00); p }

    fn patch_rel8_from_here(&mut self, pos: u16) {
        let target = self.offset();
        let rel = (target as i32 - pos as i32 - 2) as i8;
        self.bytes[pos as usize + 1] = rel as u8;
    }

    // ── CALL rel16 ────────────────────────────────────────────────
    fn call_rel16(&mut self, rel: u16) -> &mut Self { self.emit(0xE8).emit16(rel) }

    // ── PUSHA/POPA ────────────────────────────────────────────────
    fn pusha(&mut self) -> &mut Self { self.emit(0x60) }
    fn popa(&mut self) -> &mut Self { self.emit(0x61) }

    // ── CMP BX, imm16 ─────────────────────────────────────────────
    fn cmp_bx(&mut self, v: u16) -> &mut Self { self.emit(0x81).emit(0xFB).emit16(v) }

    // ── LGDT [disp16] ─────────────────────────────────────────────
    fn lgdt_mem16(&mut self, addr: u16) -> &mut Self {
        // 0F 01 /2: LGDT m32&16
        self.emit(0x0F).emit(0x01).emit(0x16).emit16(addr)
    }

    // ── MOV EAX, CR0 / MOV CR0, EAX ───────────────────────────────
    fn mov_eax_cr0(&mut self) -> &mut Self {
        self.emit(0x0F).emit(0x20).emit(0xC0) // MOV EAX, CR0
    }
    fn mov_cr0_eax(&mut self) -> &mut Self {
        self.emit(0x0F).emit(0x22).emit(0xC0) // MOV CR0, EAX
    }

    // ── OR EAX, imm32 (66 0D with 32-bit imm) ─────────────────────
    fn or_eax_imm32(&mut self, v: u32) -> &mut Self {
        self.emit(0x66).emit(0x0D).emit32(v)
    }

    // ── Far JMP 32-bit (66 EA) ────────────────────────────────────
    fn jmp_far32(&mut self, sel: u16, off: u32) -> &mut Self {
        self.emit(0x66).emit(0xEA).emit32(off).emit16(sel)
    }

    // ── 32-bit MOV r32, imm32 (66 B8+reg) ─────────────────────────
    fn mov_eax_imm32(&mut self, v: u32) -> &mut Self {
        self.emit(0x66).emit(0xB8).emit32(v)
    }
    fn mov_esi_imm32(&mut self, v: u32) -> &mut Self {
        self.emit(0x66).emit(0xBE).emit32(v)
    }
    fn mov_edi_imm32(&mut self, v: u32) -> &mut Self {
        self.emit(0x66).emit(0xBF).emit32(v)
    }
    fn mov_ecx_imm32(&mut self, v: u32) -> &mut Self {
        self.emit(0x66).emit(0xB9).emit32(v)
    }

    // ── 16-bit MOV reg, seg ───────────────────────────────────────
    fn mov_ax_ds(&mut self) -> &mut Self {
        self.emit(0x8C).emit(0xD8) // MOV AX, DS
    }

    // ── 32-bit STOSD (AB) ─────────────────────────────────────────
    fn stosd(&mut self) -> &mut Self { self.emit(0x66).emit(0xAB) }

    // ── 32-bit ADD ESI, imm8 (66 83 C6) ───────────────────────────
    fn add_esi_imm8(&mut self, v: i8) -> &mut Self {
        self.emit(0x66).emit(0x83).emit(0xC6).emit(v as u8)
    }

    // ── Label ─────────────────────────────────────────────────────
    fn label(&self) -> u16 { self.offset() }

    // ── Write raw bytes ───────────────────────────────────────────
    fn extend(&mut self, data: &[u8]) {
        self.bytes.extend_from_slice(data);
    }
}

// ═══════════════════════════════════════════════════════════════════════════
//  MBR Boot Sector (440 bytes) — real-mode, 16-bit
// ═══════════════════════════════════════════════════════════════════════════

fn build_mbr_boot_sector() -> Vec<u8> {
    let mut c = CodeBuf::new();

    // ── Initial setup at 0x7C00 ────────────────────────────────────
    c.cli(); c.xor_ax_ax(); c.mov_ss_ax(); c.mov_sp(0x7C00); c.mov_ds_ax(); c.mov_es_ax(); c.cld();
    // Copy to 0x0600
    c.mov_si(0x7C00); c.mov_di(0x0600); c.mov_cx(0x0100); c.rep_movsw();
    // Jump to relocated copy
    c.jmp_far(0x0000, 0x061E_u16);
    while c.offset() < 0x001E { c.nop(); }

    // ── Relocated code at 0x0000:0x061E ───────────────────────────
    c.sti();
    let disk_num_addr: u16  = 0x07B0;
    let use_lba_addr: u16   = 0x07B1;
    let dap_sector_count_addr: u16 = 0x07C2;
    let dap_lba_low_addr: u16  = 0x07C8;
    let dap_addr: u16       = 0x07C0;

    c.mov_mem8_dl(disk_num_addr);

    // Check LBA extensions
    c.mov_ah(0x41); c.mov_bx(0x55AA); c.int(0x13);
    let jc_pos  = c.jc_placeholder();
    c.cmp_bx(0xAA55);
    let jne_pos = c.jne_placeholder();
    c.test_cl(0x01);
    let jz_pos  = c.jz_placeholder();
    c.mov_mem8_imm8(use_lba_addr, 1);

    // no_lba:
    let no_lba = c.label();
    c.patch_rel8_from_here(jc_pos);
    c.patch_rel8_from_here(jne_pos);
    c.patch_rel8_from_here(jz_pos);

    // Set up DAP
    c.mov_mem32_imm32(dap_lba_low_addr, 1);
    c.mov_mem16_imm16(dap_sector_count_addr, 127);

    c.cmp_mem8_imm8(use_lba_addr, 1);
    let jne_chs = c.jne_placeholder();

    // LBA read loop
    let lba_read = c.label();
    c.mov_ah(0x42); c.mov_dl_mem16(disk_num_addr); c.mov_si(dap_addr); c.int(0x13);
    let jnc_ok = c.jnc_placeholder();
    c.shr_mem16_1(dap_sector_count_addr);
    let jnz_retry = c.jne_placeholder();
    // fall through → error

    // chs (error fallback):
    let chs = c.label();
    c.patch_rel8_from_here(jne_chs);
    c.mov_si(0x07B2);
    let _ = c.offset();
    c.call_rel16(0); // placeholder
    let call_rel_pos = c.offset();
    c.jmp_short(-2i8);

    // retry patch
    c.patch_rel8_from_here(jnz_retry);

    // load_ok:
    let load_ok = c.label();
    c.patch_rel8_from_here(jnc_ok);
    c.mov_dl_mem16(disk_num_addr);
    c.jmp_far(0x0000, 0x7E00);

    // print_string subroutine
    let print_label = c.label();
    let call_instr = call_rel_pos - 2;
    c.bytes[call_instr as usize + 1] = (print_label - call_rel_pos) as u8;
    c.bytes[call_instr as usize + 2] = ((print_label - call_rel_pos) >> 8) as u8;

    c.pusha();
    let ploop = c.label();
    c.mov_ah(0x0E); c.xor_bx_bx(); c.lodsb(); c.test_al_al();
    let pjz = c.jz_placeholder();
    c.int(0x10);
    c.jmp_short(-9i8);
    c.popa(); c.ret();
    c.patch_rel8_from_here(pjz);

    while c.bytes.len() < 440 { c.emit(0x00); }
    c.bytes
}

// ═══════════════════════════════════════════════════════════════════════════
//  Stage2 — real-mode → protected-mode → 32-bit flat binary
// ═══════════════════════════════════════════════════════════════════════════

fn build_stage2_binary() -> Vec<u8> {
    let mut c = CodeBuf::new();

    // ── Stage2 entry: 0x0000:0x7E00, DL = disk number ─────────────
    c.cli();

    // Save disk number
    let disk_byte_addr: u16 = 0x7DFE;
    c.mov_mem8_dl(disk_byte_addr);

    // Enable A20 via keyboard controller
    c.mov_al(0xD1);
    c.emit(0xE6).emit(0x64);
    c.mov_al(0xDF);
    c.emit(0xE6).emit(0x60);

    // GDT pointer and entries are at fixed offsets within stage2
    let gdt_ptr_off: u16   = 0x80; // offset of GDT pseudo-descriptor
    let gdt_entries_off: u16 = 0x86; // offset of GDT entries (6 bytes after ptr)
    let prot_entry_off: u16 = 0xA0; // offset of 32-bit PM entry point

    // Load GDT (LGDT [gdt_ptr])
    c.lgdt_mem16(0x7E00 + gdt_ptr_off);

    // Set PE bit in CR0
    c.mov_eax_cr0();
    c.or_eax_imm32(1);
    c.mov_cr0_eax();

    // Far jump to 32-bit code segment (selector 0x08)
    let pm_jmp_target: u32 = (0x7E00 + prot_entry_off as u16) as u32;
    c.jmp_far32(0x08, pm_jmp_target);

    // ── Fill to GDT pointer offset ─────────────────────────────────
    while c.offset() < gdt_ptr_off { c.emit(0x90); }

    // GDT pseudo-descriptor: [limit: u16, base: u32]
    c.emit16(3 * 8 - 1);                // limit = 23
    c.emit32(0x7E00 + gdt_entries_off as u32);  // base

    // GDT entries
    while c.offset() < gdt_entries_off { c.emit(0x00); }

    // Entry 0: null
    c.emit32(0); c.emit32(0);

    // Entry 1 (sel 0x08): 32-bit code, base=0, limit=4GB
    c.emit16(0xFFFF); c.emit16(0x0000); c.emit(0x00);
    c.emit(0x9A); c.emit(0xCF); c.emit(0x00);

    // Entry 2 (sel 0x10): 32-bit data, base=0, limit=4GB
    c.emit16(0xFFFF); c.emit16(0x0000); c.emit(0x00);
    c.emit(0x92); c.emit(0xCF); c.emit(0x00);

    // ── Fill to protected-mode entry ──────────────────────────────
    while c.offset() < prot_entry_off { c.emit(0x90); }

    // ── 32-bit protected mode ─────────────────────────────────────
    // Setup data segments (sel 0x10)
    c.emit(0x66).emit(0xB8).emit16(0x0010); // MOV EAX, 0x10
    c.emit(0x8E).emit(0xD8); // MOV DS, EAX
    c.emit(0x8E).emit(0xC0); // MOV ES, EAX
    c.emit(0x8E).emit(0xE0); // MOV FS, EAX
    c.emit(0x8E).emit(0xE8); // MOV GS, EAX
    c.emit(0x8E).emit(0xD0); // MOV SS, EAX
    c.emit(0x66).emit(0xBC).emit32(0x0008_0000u32); // MOV ESP, 0x80000

    // ── Clear VGA text buffer (0xB8000, 80x25) ───────────────────
    c.emit(0x66).emit(0xBF).emit32(0x000B_8000u32); // MOV EDI, 0xB8000
    c.emit(0x66).emit(0xB9).emit32(80 * 25);         // MOV ECX, 2000
    c.emit(0x66).emit(0xB8).emit32(0x0720_0720u32);  // MOV EAX, attr:space
    // clear_loop:
    let clear_loop = c.offset() as usize;
    c.emit(0x66).emit(0xAB);     // STOSD
    c.emit(0x66).emit(0x49);     // DEC ECX
    let jnz_pos = c.offset();
    c.emit(0x75).emit(0x00);     // JNZ (placeholder)
    c.bytes[jnz_pos as usize + 1] = (clear_loop as i32 - jnz_pos as i32 - 2) as i8 as u8;

    // ── Print strings via helper ───────────────────────────────────
    vga_print_str(&mut c, 2*80+0,  b"  Choosable Stage2 Bootloader  ", 0x1F);
    vga_print_str(&mut c, 2*80+4,  b"================================  ", 0x17);
    vga_print_str(&mut c, 4*80+6,  b"Stage2 in 32-bit protected mode!", 0x0A);
    vga_print_str(&mut c, 6*80+10, b"Scanning for ISO files...", 0x07);

    // ── Halt ───────────────────────────────────────────────────────
    c.hlt();
    c.jmp_short(-2i8);

    while c.bytes.len() < 512 { c.emit(0x00); }
    c.bytes
}

/// Write an ASCII string to VGA text buffer at column/row using inline code.
fn vga_print_str(c: &mut CodeBuf, offset_words: u32, s: &[u8], attr: u8) {
    // EDI = 0xB8000 + offset * 2
    c.emit(0x66).emit(0xBF).emit32(0x000B_8000u32 + offset_words * 2);
    let attr_word: u16 = ((attr as u16) << 8) | 0x20;
    for &ch in s {
        if ch == b'\0' {
            break;
        }
        let word: u16 = ((attr as u16) << 8) | (ch as u16);
        c.emit(0x66).emit(0xC7).emit(0x07).emit16(word); // MOV DWORD [EDI], imm32
        c.emit(0x66).emit(0x83).emit(0xC7).emit(0x02);    // ADD EDI, 2
    }
}

// ═══════════════════════════════════════════════════════════════════════════
//  EFI Bootloader (cross-compiled via cargo)
// ═══════════════════════════════════════════════════════════════════════════

fn build_efi_binary(out_dir: &PathBuf) -> Vec<u8> {
    let efi_dir = PathBuf::from(env::var("CARGO_MANIFEST_DIR").unwrap()).join("efi");
    let efi_target_dir = out_dir.join("efi-target");

    println!("cargo:warning=Building EFI bootloader for x86_64-unknown-uefi...");

    let status = std::process::Command::new("cargo")
        .args(&[
            "build",
            "--target", "x86_64-unknown-uefi",
            "--release",
            "--target-dir",
        ])
        .arg(&efi_target_dir)
        .current_dir(&efi_dir)
        .status();

    match status {
        Ok(s) if s.success() => {}
        Ok(s) => {
            println!("cargo:warning=EFI build failed with exit code {:?}, using placeholder", s.code());
            return vec![0; 512];
        }
        Err(e) => {
            println!("cargo:warning=Failed to run cargo for EFI build: {}, using placeholder", e);
            return vec![0; 512];
        }
    }

    let efi_path = efi_target_dir
        .join("x86_64-unknown-uefi")
        .join("release")
        .join("choosable-efi.efi");

    match fs::read(&efi_path) {
        Ok(data) => {
            println!("cargo:warning=EFI binary size: {} bytes", data.len());
            data
        }
        Err(e) => {
            println!("cargo:warning=Failed to read EFI binary '{}': {}", efi_path.display(), e);
            vec![0; 512]
        }
    }
}