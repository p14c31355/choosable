//! Build script: generates bootloader binaries as Rust byte arrays.
//!
//! Outputs `src/generated.rs` containing:
//! - `BOOT_IMG`: 440-byte MBR boot sector (x86 machine code)
//! - `STAGE2_BIN`: BIOS stage2 flat binary (placeholder)
//! - `EFI_BIN`: UEFI PE binary (placeholder)
//!
//! No external tools (nasm, gcc, etc.) required — all machine code
//! is constructed from pure Rust opcode tables.

use std::env;
use std::fs;
use std::path::PathBuf;

fn main() {
    let out_dir = PathBuf::from(env::var("OUT_DIR").unwrap());
    fs::create_dir_all(out_dir.join("out")).ok();
    let dest = out_dir.join("generated.rs");

    let boot_img = build_mbr_boot_sector();
    let stage2_bin = build_stage2_binary();
    let efi_bin = build_efi_binary();

    let code = format!(
        r#"// AUTO-GENERATED by build.rs — DO NOT EDIT

/// MBR boot sector (440 bytes of x86 real-mode machine code).
/// Bytes 0-439: boot code, bytes 440-445: zero (installer fills disk signature).
pub const BOOT_IMG: &[u8] = &{boot_img:?};

/// Stage 2 BIOS bootloader flat binary.
pub const STAGE2_BIN: &[u8] = &{stage2_bin:?};

/// UEFI bootloader PE binary (BOOTX64.EFI).
pub const EFI_BIN: &[u8] = &{efi_bin:?};
"#,
    );

    fs::write(&dest, &code).unwrap();
    println!("cargo:rerun-if-changed=build.rs");
    println!("cargo:rerun-if-changed=src/stage2.rs");
    println!("cargo:rerun-if-changed=src/efi.rs");
}

// ── x86-16 instruction encoder (tiny subset) ─────────────────────────────

struct CodeBuf {
    bytes: Vec<u8>,
}

impl CodeBuf {
    fn new() -> Self {
        CodeBuf { bytes: Vec::with_capacity(512) }
    }

    fn offset(&self) -> u16 {
        self.bytes.len() as u16
    }

    fn emit(&mut self, b: u8) -> &mut Self {
        self.bytes.push(b);
        self
    }

    fn emit16(&mut self, w: u16) -> &mut Self {
        self.bytes.push(w as u8);
        self.bytes.push((w >> 8) as u8);
        self
    }

    fn emit32(&mut self, d: u32) -> &mut Self {
        self.bytes.push(d as u8);
        self.bytes.push((d >> 8) as u8);
        self.bytes.push((d >> 16) as u8);
        self.bytes.push((d >> 24) as u8);
        self
    }

    fn cli(&mut self) -> &mut Self { self.emit(0xFA) }
    fn sti(&mut self) -> &mut Self { self.emit(0xFB) }
    fn cld(&mut self) -> &mut Self { self.emit(0xFC) }
    fn hlt(&mut self) -> &mut Self { self.emit(0xF4) }

    // XOR reg, reg
    fn xor_ax_ax(&mut self) -> &mut Self { self.emit(0x31).emit(0xC0) }
    fn xor_bx_bx(&mut self) -> &mut Self { self.emit(0x31).emit(0xDB) }

    // MOV seg, reg16  (seg = DS:0xD8, ES:0xC0, SS:0xD0)
    fn mov_ss_ax(&mut self) -> &mut Self { self.emit(0x8E).emit(0xD0) }
    fn mov_ds_ax(&mut self) -> &mut Self { self.emit(0x8E).emit(0xD8) }
    fn mov_es_ax(&mut self) -> &mut Self { self.emit(0x8E).emit(0xC0) }

    // MOV reg16, imm16
    fn mov_sp(&mut self, v: u16) -> &mut Self { self.emit(0xBC).emit16(v) }
    fn mov_si(&mut self, v: u16) -> &mut Self { self.emit(0xBE).emit16(v) }
    fn mov_di(&mut self, v: u16) -> &mut Self { self.emit(0xBF).emit16(v) }
    fn mov_cx(&mut self, v: u16) -> &mut Self { self.emit(0xB9).emit16(v) }

    // MOV AH, imm8
    fn mov_ah(&mut self, v: u8) -> &mut Self { self.emit(0xB4).emit(v) }

    // MOV BX, imm16
    fn mov_bx(&mut self, v: u16) -> &mut Self { self.emit(0xBB).emit16(v) }

    // MOV reg16, [disp16]  (0x8B + modrm)
    fn mov_dl_mem16(&mut self, addr: u16) -> &mut Self {
        self.emit(0x8A).emit(0x16).emit16(addr)
    }

    // MOV byte [disp16], reg8
    fn mov_mem8_dl(&mut self, addr: u16) -> &mut Self {
        self.emit(0x88).emit(0x16).emit16(addr)
    }

    // MOV byte [disp16], imm8
    fn mov_mem8_imm8(&mut self, addr: u16, v: u8) -> &mut Self {
        self.emit(0xC6).emit(0x06).emit16(addr).emit(v)
    }

    // MOV word [disp16], imm16
    fn mov_mem16_imm16(&mut self, addr: u16, v: u16) -> &mut Self {
        self.emit(0xC7).emit(0x06).emit16(addr).emit16(v)
    }

    // MOV dword [disp16], imm32 (operand size prefix 0x66)
    fn mov_mem32_imm32(&mut self, addr: u16, v: u32) -> &mut Self {
        self.emit(0x66).emit(0xC7).emit(0x06).emit16(addr).emit32(v)
    }

    // CMP byte [disp16], imm8
    fn cmp_mem8_imm8(&mut self, addr: u16, v: u8) -> &mut Self {
        self.emit(0x80).emit(0x3E).emit16(addr).emit(v)
    }

    // TEST CL, imm8
    fn test_cl(&mut self, v: u8) -> &mut Self {
        self.emit(0xF6).emit(0xC1).emit(v)
    }

    // SHR word [disp16], 1
    fn shr_mem16_1(&mut self, addr: u16) -> &mut Self {
        self.emit(0xD1).emit(0x2E).emit16(addr)
    }

    // REP MOVSW
    fn rep_movsw(&mut self) -> &mut Self { self.emit(0xF3).emit(0xA5) }

    // INT imm8
    fn int(&mut self, n: u8) -> &mut Self { self.emit(0xCD).emit(n) }

    // JMP FAR seg:off
    fn jmp_far(&mut self, seg: u16, off: u16) -> &mut Self {
        self.emit(0xEA).emit16(off).emit16(seg)
    }

    // JMP rel8 (short)
    fn jmp_short(&mut self, rel: i8) -> &mut Self {
        self.emit(0xEB).emit(rel as u8)
    }

    // JMP rel16 (near)
    fn jmp_near(&mut self, rel: u16) -> &mut Self {
        self.emit(0xE9).emit16(rel)
    }

    // JC rel8
    fn jc(&mut self, rel: i8) -> &mut Self {
        self.emit(0x72).emit(rel as u8)
    }

    // JNC rel8
    fn jnc(&mut self, rel: i8) -> &mut Self {
        self.emit(0x73).emit(rel as u8)
    }

    // JNE/JNZ rel8
    fn jne(&mut self, rel: i8) -> &mut Self {
        self.emit(0x75).emit(rel as u8)
    }

    // JZ rel8
    fn jz(&mut self, rel: i8) -> &mut Self {
        self.emit(0x74).emit(rel as u8)
    }

    // CALL rel16 (offset relative to next instruction)
    fn call_rel16(&mut self, rel: u16) -> &mut Self {
        self.emit(0xE8).emit16(rel)
    }

    // NOP
    fn nop(&mut self) -> &mut Self { self.emit(0x90) }

    // Ret (near)
    fn ret(&mut self) -> &mut Self { self.emit(0xC3) }

    // CMP BX, imm16
    fn cmp_bx(&mut self, v: u16) -> &mut Self {
        self.emit(0x81).emit(0xFB).emit16(v)
    }

    // LODSB
    fn lodsb(&mut self) -> &mut Self { self.emit(0xAC) }

    // OR AL, AL (test AL)
    fn test_al_al(&mut self) -> &mut Self { self.emit(0x08).emit(0xC0) }

    // PUSHA / POPA
    fn pusha(&mut self) -> &mut Self { self.emit(0x60) }
    fn popa(&mut self) -> &mut Self { self.emit(0x61) }

    // Label placeholder: returns current offset
    fn label(&self) -> u16 { self.offset() }

    // Placeholder for short-jump target: emit 0 and return location to patch
    fn placeholder_jmp_short(&mut self) -> u16 {
        let pos = self.offset();
        self.emit(0xEB).emit(0x00); // JMP +0 (placeholder)
        pos
    }

    // Patch a short-jump at `pos` (points to JMP opcode; rel8 is at pos+1)
    fn patch_jmp_short_to_here(&mut self, jmp_pos: u16) {
        let target = self.offset();
        let rel = (target as i32 - jmp_pos as i32 - 2) as i8;
        self.bytes[jmp_pos as usize + 1] = rel as u8;
    }

    // Patch a short conditional jump (JC/JNC/JZ/JNE at pos, rel8 at pos+1)
    fn patch_jcc_short_to_here(&mut self, jcc_pos: u16) {
        let target = self.offset();
        let rel = (target as i32 - jcc_pos as i32 - 2) as i8;
        self.bytes[jcc_pos as usize + 1] = rel as u8;
    }
}

// ─── MBR Boot Sector Generator ───────────────────────────────────────────

/// Build the 440-byte MBR boot sector.
///
/// Memory layout after BIOS loads us:
///   CS:IP = 0x0000:0x7C00
///   DL = boot disk number
///
/// The code:
///  1. Sets up real-mode segments
///  2. Relocates from 0x7C00 to 0x0600 (to make room for stage2 at 0x7E00)
///  3. Checks for LBA BIOS extensions (INT 13h AH=41h)
///  4. Loads stage2 from sectors 1..128 to 0x7E00
///  5. Jumps to stage2 at 0x0000:0x7E00
fn build_mbr_boot_sector() -> Vec<u8> {
    let mut c = CodeBuf::new();

    // ── Phase 1: Initial setup (runs at 0x7C00) ─────────────────────
    c.cli();
    c.xor_ax_ax();
    c.mov_ss_ax();
    c.mov_sp(0x7C00);       // stack grows down from 0x7C00
    c.mov_ds_ax();
    c.mov_es_ax();
    c.cld();

    // Copy 512 bytes from 0x7C00 to 0x0600
    c.mov_si(0x7C00);
    c.mov_di(0x0600);
    c.mov_cx(0x0100);       // 256 words = 512 bytes
    c.rep_movsw();

    // Jump to relocated copy at 0x0000:0x061E
    // 0x061E = offset of the relocated STI instruction
    let relocated_entry = 0x061E_u16;
    c.jmp_far(0x0000, relocated_entry);

    // ── Padding to align relocated code ─────────────────────────────
    // Ensure the next instruction is at physical address 0x061E
    // We need offset 0x001E in the binary (BIOS loads at 0x7C00, physical 0x7C1E)
    while c.offset() < 0x001E {
        c.nop();
    }

    // ── Phase 2: Relocated code (runs at 0x0000:0x061E) ────────────
    c.sti();

    // Save disk number (DL) at disk_num variable
    // Variables are at the end of the sector: 0x0600 + 0x01B0 = 0x07B0 physical
    // But BIOS sees DS=0, so address = physical
    let disk_num_addr: u16 = 0x07B0;
    let use_lba_addr: u16 = 0x07B1;
    let dap_sector_count_addr: u16 = 0x07C2;
    let dap_lba_low_addr: u16 = 0x07C8;
    let dap_addr: u16 = 0x07C0;

    c.mov_mem8_dl(disk_num_addr);

    // ── Check for LBA extensions (INT 13h AH=41h) ──────────────────
    c.mov_ah(0x41);
    c.mov_bx(0x55AA);
    c.int(0x13);
    let jc_no_lba_pos = c.offset(); // will be patched
    c.emit(0x72).emit(0x00);        // JC no_lba (placeholder)
    c.cmp_bx(0xAA55);
    let jne_no_lba_pos = c.offset();
    c.emit(0x75).emit(0x00);        // JNE no_lba (placeholder)
    c.test_cl(0x01);                // bit 0: extended disk access functions
    let jz_no_lba_pos = c.offset();
    c.emit(0x74).emit(0x00);        // JZ no_lba (placeholder)

    // LBA supported
    c.mov_mem8_imm8(use_lba_addr, 1);

    // ── no_lba label ───────────────────────────────────────────────
    let no_lba_pos = c.label();
    // Patch conditional jumps to here
    c.bytes[jc_no_lba_pos as usize + 1] = (no_lba_pos as i32 - jc_no_lba_pos as i32 - 2) as i8 as u8;
    c.bytes[jne_no_lba_pos as usize + 1] = (no_lba_pos as i32 - jne_no_lba_pos as i32 - 2) as i8 as u8;
    c.bytes[jz_no_lba_pos as usize + 1] = (no_lba_pos as i32 - jz_no_lba_pos as i32 - 2) as i8 as u8;

    // ── Set up DAP for reading stage2 ──────────────────────────────
    // DAP is at offset 0x07C0 (512-64 = 0x1C0 from start of sector)
    // DAP layout:
    //   +0: size (0x10)
    //   +1: reserved (0)
    //   +2: sector count (word)
    //   +4: buffer offset (0x7E00)
    //   +6: buffer segment (0x0000)
    //   +8: LBA low (dword)
    //   +12: LBA high (dword, always 0)

    // We need to write these values into the fixed area at 0x07C0.
    // But at build time we're generating the binary, so we set them up
    // as instructions that write to those addresses at runtime.

    // Actually, these are data initialized at compile time in the binary.
    // Let's reserve space for them at the data area and use those addresses.

    // For now, set LBA = 1 (sector 1), sector_count = 127
    c.mov_mem32_imm32(dap_lba_low_addr, 1);
    c.mov_mem16_imm16(dap_sector_count_addr, 127);

    // ── Check if LBA is available ──────────────────────────────────
    c.cmp_mem8_imm8(use_lba_addr, 1);
    let jne_chs_pos = c.offset();
    c.emit(0x75).emit(0x00);        // JNE chs_read (placeholder)

    // ── LBA read ───────────────────────────────────────────────────
    let lba_read_pos = c.label();

    c.mov_ah(0x42);                 // Extended read
    c.mov_dl_mem16(disk_num_addr);
    c.mov_si(dap_addr);             // DS:SI -> DAP
    c.int(0x13);
    let jnc_ok_pos = c.offset();
    c.emit(0x73).emit(0x00);        // JNC load_ok (placeholder)

    // Read failed — retry with half the sectors
    c.shr_mem16_1(dap_sector_count_addr);
    let jnz_retry_pos = c.offset();
    c.emit(0x75).emit(0x00);        // JNZ lba_read (placeholder)
    // Fall through to error

    // ── CHS read (unlikely on modern HW) ───────────────────────────
    let chs_read_pos = c.label();
    // Patch JNE to here
    c.bytes[jne_chs_pos as usize + 1] = (chs_read_pos as i32 - jne_chs_pos as i32 - 2) as i8 as u8;

    // Simple error: print message and halt
    c.mov_si(0x07B2);               // msg_no_lba at 0x07B2
    let _call_print_pos = c.offset();
    c.emit(0xE8).emit16(0x0000);    // CALL print_string (placeholder)
    let call_print_rel = c.offset();

    // Halt
    c.jmp_short(-2i8);              // infinite loop

    // ── Retry (patch JNZ to lba_read) ──────────────────────────────
    let _after_chs = c.label();
    c.bytes[jnz_retry_pos as usize + 1] = (lba_read_pos as i32 - jnz_retry_pos as i32 - 2) as i8 as u8;

    // ── load_ok ────────────────────────────────────────────────────
    let load_ok_pos = c.label();
    // Patch JNC to here
    c.bytes[jnc_ok_pos as usize + 1] = (load_ok_pos as i32 - jnc_ok_pos as i32 - 2) as i8 as u8;

    // Pass disk number in DL and jump to stage2 at 0x0000:0x7E00
    c.mov_dl_mem16(disk_num_addr);
    c.jmp_far(0x0000, 0x7E00);

    // ── print_string subroutine ─────────────────────────────────────
    let print_start = c.label();
    // Patch CALL above to point here
    let call_instr_pos = call_print_rel - 2; // CALL opcode position
    c.bytes[call_instr_pos as usize + 1] = (print_start as u16 - call_print_rel) as u8;
    c.bytes[call_instr_pos as usize + 2] = ((print_start as u16 - call_print_rel) >> 8) as u8;

    // PUSHA
    c.pusha();
    // print_loop:
    let _print_loop = c.label();
    c.mov_ah(0x0E);                 // BIOS teletype output
    c.xor_bx_bx();                  // page 0
    c.lodsb();                      // AL = [DS:SI++]
    c.test_al_al();                 // test AL, AL
    let jz_print_done = c.offset();
    c.emit(0x74).emit(0x00);        // JZ done (placeholder)
    c.int(0x10);                    // INT 10h
    c.jmp_short(-9i8);              // back to print_loop
    // done:
    c.popa();
    c.ret();

    // Patch JZ to ret
    let done_pos = c.label();
    c.bytes[jz_print_done as usize + 1] = (done_pos as i32 - jz_print_done as i32 - 2) as i8 as u8;

    // ── Fill to 440 bytes ──────────────────────────────────────────
    while c.bytes.len() < 440 {
        c.emit(0x00);
    }

    c.bytes
}

// ─── Stage2 Placeholder ──────────────────────────────────────────────────

fn build_stage2_binary() -> Vec<u8> {
    // Placeholder — will be replaced with real stage2 binary.
    // For now, return a tiny binary that just prints "STAGE2" and halts.
    let mut c = CodeBuf::new();

    // Simple stage2 test: print message via BIOS
    c.sti();
    c.mov_si(0x7E00 + 0x20); // message offset within stage2
    // print loop
    let _loop_pos = c.label();
    c.mov_ah(0x0E);
    c.xor_bx_bx();
    c.lodsb();
    c.test_al_al();
    let jz_done = c.offset();
    c.emit(0x74).emit(0x00);
    c.int(0x10);
    c.jmp_short(-11i8);
    let done_pos_2 = c.label();
    c.bytes[jz_done as usize + 1] = (done_pos_2 as i32 - jz_done as i32 - 2) as i8 as u8;
    c.hlt();
    c.jmp_short(-2i8);

    // Padding + message
    while c.bytes.len() < 0x20 {
        c.emit(0x00);
    }
    // Message at offset 0x20
    c.bytes.extend_from_slice(b"STAGE2 OK\r\n\0");

    // Pad to at least 512 bytes
    while c.bytes.len() < 512 {
        c.emit(0x00);
    }

    c.bytes
}

